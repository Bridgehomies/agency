// ─── Helpers ─────────────────────────────────────────────────────────────────

function slugify(text: string): string {
  return text
    .toLowerCase()
    .replace(/[^\w\s-]/g, "")
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-")
    .trim()
    .slice(0, 80);
}

function generateId(): string {
  return Math.random().toString(36).slice(2, 10);
}

function estimateReadTime(content: string): string {
  const words = content.replace(/<[^>]+>/g, "").split(/\s+/).length;
  const mins = Math.max(1, Math.round(words / 200));
  return `${mins} min`;
}

function formatDate(iso: string): string {
  return new Date(iso).toLocaleDateString("en-US", {
    year: "numeric",
    month: "short",
    day: "numeric",
  });
}

function formatViews(n: number): string {
  return n >= 1000 ? `${(n / 1000).toFixed(1)}K` : String(n);
}

// ─── Schema.org JSON-LD generator ────────────────────────────────────────────

function generateStructuredData(post: BlogPost, siteUrl: string): string {
  const schema = {
    "@context": "https://schema.org",
    "@type": post.schemaType,
    headline: post.seoTitle || post.title,
    description: post.seoDescription || post.excerpt,
    image: post.coverImage,
    datePublished: post.date,
    dateModified: post.date,
    author: {
      "@type": "Person",
      name: post.author,
      jobTitle: post.authorRole,
    },
    publisher: {
      "@type": "Organization",
      name: "Bridge Homies",
      url: siteUrl,
    },
    url: `${siteUrl}/blog#${post.slug}`,
    keywords: post.tags.join(", "),
    articleSection: post.category,
    mainEntityOfPage: {
      "@type": "WebPage",
      "@id": `${siteUrl}/blog#${post.slug}`,
    },
  };
  return JSON.stringify(schema, null, 2);
}

// ─── Storage helpers ──────────────────────────────────────────────────────────

const STORAGE_KEY = "bh_blog_posts";

function loadPosts(): BlogPost[] {
  try {
    const raw = localStorage.getItem(STORAGE_KEY);
    if (!raw) return SEED_POSTS;
    const parsed: BlogPost[] = JSON.parse(raw);
    return parsed.length ? parsed : SEED_POSTS;
  } catch {
    return SEED_POSTS;
  }
}

function savePosts(posts: BlogPost[]): void {
  try {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(posts));
  } catch {
    // silent
  }
}

