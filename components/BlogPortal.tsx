// ─── Login gate ───────────────────────────────────────────────────────────────

function AdminLogin({ onSuccess }: { onSuccess: () => void }) {
  const [pass, setPass] = useState("");
  const [error, setError] = useState(false);

  function submit(e: React.FormEvent) {
    e.preventDefault();
    if (pass === ADMIN_PASS) {
      sessionStorage.setItem("bh_admin", "1");
      onSuccess();
    } else {
      setError(true);
    }
  }

  return (
    <div className="min-h-[60vh] flex items-center justify-center">
      <div className="w-full max-w-sm">
        <h2 className="text-2xl font-bold text-gray-900 mb-6 text-center">Admin access</h2>
        <form onSubmit={submit} className="space-y-4">
          <input
            type="password"
            placeholder="Password"
            value={pass}
            onChange={(e) => { setPass(e.target.value); setError(false); }}
            autoFocus
            className={`w-full border ${error ? "border-red-400" : "border-gray-200"} rounded-lg px-4 py-3 text-sm focus:outline-none focus:ring-2 focus:ring-purple-400`}
          />
          {error && <p className="text-sm text-red-500">Incorrect password</p>}
          <button
            type="submit"
            className="w-full bg-purple-600 hover:bg-purple-700 text-white font-medium py-3 rounded-lg transition-colors"
          >
            Sign in
          </button>
        </form>
        <p className="text-xs text-center text-gray-400 mt-4">
          Demo password: <code className="bg-gray-100 px-1.5 py-0.5 rounded">bridgehomies2025</code>
        </p>
      </div>
    </div>
  );
}

// ─── Main BlogPortal ──────────────────────────────────────────────────────────

export default function BlogPortal() {
  const [posts, setPosts] = useState<BlogPost[]>([]);
  const [view, setView] = useState<ActiveView>("listing");
  const [activePost, setActivePost] = useState<BlogPost | null>(null);
  const [editingPost, setEditingPost] = useState<BlogPost | null>(null);
  const [searchQuery, setSearchQuery] = useState("");
  const [activeCategory, setActiveCategory] = useState("All");
  const [adminAuthed, setAdminAuthed] = useState(false);
  const [mounted, setMounted] = useState(false);

  // Load from localStorage
  useEffect(() => {
    const stored = loadPosts();
    setPosts(stored);
    setAdminAuthed(sessionStorage.getItem("bh_admin") === "1");
    setMounted(true);
  }, []);

  // Handle URL hash routing
  useEffect(() => {
    if (!mounted) return;

    function handleHashChange() {
      const hash = window.location.hash.slice(1); // remove #
      if (!hash) {
        setView("listing");
        setActivePost(null);
        return;
      }
      if (hash === "admin") {
        setView("admin");
        return;
      }
      // Try to find a post by slug
      const found = posts.find((p) => p.slug === hash);
      if (found) {
        setActivePost(found);
        setView("article");
        // Increment view counter
        setPosts((prev) => {
          const updated = prev.map((p) =>
            p.id === found.id ? { ...p, views: p.views + 1 } : p
          );
          savePosts(updated);
          return updated;
        });
      }
    }

    handleHashChange();
    window.addEventListener("hashchange", handleHashChange);
    return () => window.removeEventListener("hashchange", handleHashChange);
  }, [mounted, posts]);

  function openPost(post: BlogPost) {
    window.location.hash = post.slug;
  }

  function goBack() {
    window.location.hash = "";
    setView("listing");
    setActivePost(null);
  }

  // ── CRUD ──────────────────────────────────────────────────────────────────

  function handleSave(post: BlogPost) {
    setPosts((prev) => {
      const exists = prev.find((p) => p.id === post.id);
      const updated = exists
        ? prev.map((p) => (p.id === post.id ? post : p))
        : [...prev, post];
      savePosts(updated);
      return updated;
    });
    setView("admin");
    setEditingPost(null);
  }

  function handleDelete(id: string) {
    setPosts((prev) => {
      const updated = prev.filter((p) => p.id !== id);
      savePosts(updated);
      return updated;
    });
  }

  function handleTogglePublish(id: string) {
    setPosts((prev) => {
      const updated = prev.map((p) =>
        p.id === id ? { ...p, published: !p.published } : p
      );
      savePosts(updated);
      return updated;
    });
  }

  // ── Derived data ──────────────────────────────────────────────────────────

  const publishedPosts = posts.filter((p) => p.published);

  const categories = [
    "All",
    ...Array.from(new Set(publishedPosts.map((p) => p.category).filter(Boolean))),
  ];

  const filteredPosts = publishedPosts.filter((p) => {
    const matchCat = activeCategory === "All" || p.category === activeCategory;
    const q = searchQuery.toLowerCase();
    const matchQ =
      !q ||
      p.title.toLowerCase().includes(q) ||
      p.excerpt.toLowerCase().includes(q) ||
      p.tags.some((t) => t.toLowerCase().includes(q));
    return matchCat && matchQ;
  });

  const relatedPosts = activePost
    ? publishedPosts
        .filter(
          (p) =>
            p.id !== activePost.id &&
            p.tags.some((t) => activePost.tags.includes(t))
        )
        .slice(0, 2)
    : [];

  // ── Render ────────────────────────────────────────────────────────────────

  if (!mounted) return null;

  // Admin login gate
  if (view === "admin" && !adminAuthed) {
    return (
      <div className="min-h-screen bg-white dark:bg-gray-950">
        <AdminLogin onSuccess={() => { setAdminAuthed(true); setView("admin"); }} />
      </div>
    );
  }

  if (view === "admin") {
    return (
      <div className="min-h-screen bg-white dark:bg-gray-950">
        <AdminPanel
          posts={posts}
          onNew={() => { setEditingPost(emptyPost()); setView("write"); }}
          onEdit={(p) => { setEditingPost(p); setView("write"); }}
          onDelete={handleDelete}
          onTogglePublish={handleTogglePublish}
          onBack={goBack}
        />
      </div>
    );
  }

  if (view === "write" && editingPost) {
    return (
      <div className="min-h-screen bg-white dark:bg-gray-950">
        <WritePanel
          initial={editingPost}
          onSave={handleSave}
          onCancel={() => setView("admin")}
        />
      </div>
    );
  }

  if (view === "article" && activePost) {
    return (
      <div className="min-h-screen bg-white dark:bg-gray-950">
        <ArticleView post={activePost} onBack={goBack} relatedPosts={relatedPosts} />
      </div>
    );
  }

  // ── Listing view ──────────────────────────────────────────────────────────

  return (
    <div className="min-h-screen bg-white dark:bg-gray-950">
      {/* Blog header */}
      <div className="bg-gray-950 text-white py-16 px-6">
        <div className="max-w-5xl mx-auto">
          <h1 className="text-4xl md:text-5xl font-bold mb-3">Bridge Homies Blog</h1>
          <p className="text-gray-400 text-lg max-w-xl">
            Engineering insights, product updates, and digital strategy from our team.
          </p>

          {/* Search */}
          <div className="relative mt-8 max-w-md">
            <Search className="absolute left-3.5 top-1/2 -translate-y-1/2 w-4 h-4 text-gray-400" />
            <input
              type="text"
              placeholder="Search articles…"
              value={searchQuery}
              onChange={(e) => setSearchQuery(e.target.value)}
              className="w-full pl-10 pr-4 py-3 bg-gray-800 border border-gray-700 rounded-xl text-sm text-white placeholder-gray-500 focus:outline-none focus:border-purple-500 transition-colors"
            />
          </div>
        </div>
      </div>

      {/* Category tabs */}
      <div className="border-b border-gray-200 dark:border-gray-800 bg-white dark:bg-gray-950 sticky top-0 z-10">
        <div className="max-w-5xl mx-auto px-6 overflow-x-auto">
          <div className="flex items-center gap-1 py-3 whitespace-nowrap">
            {categories.map((cat) => (
              <button
                key={cat}
                onClick={() => setActiveCategory(cat)}
                className={`px-4 py-2 rounded-full text-sm font-medium transition-all ${
                  activeCategory === cat
                    ? "bg-purple-600 text-white"
                    : "text-gray-600 hover:bg-gray-100 dark:hover:bg-gray-800"
                }`}
              >
                {cat}
              </button>
            ))}
          </div>
        </div>
      </div>

      {/* Articles grid */}
      <div className="max-w-5xl mx-auto px-6 py-12">
        {filteredPosts.length === 0 ? (
          <div className="text-center py-16 text-gray-400">
            <BookOpen className="w-10 h-10 mx-auto mb-3 opacity-40" />
            <p className="text-lg">No articles found.</p>
          </div>
        ) : (
          <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
            {filteredPosts.map((post, i) => (
              <ArticleCard
                key={post.id}
                post={post}
                onOpen={openPost}
                featured={i === 0 && filteredPosts.length >= 3}
              />
            ))}
          </div>
        )}
      </div>

      {/* Admin link */}
      <div className="border-t border-gray-200 dark:border-gray-800 py-6 px-6 text-center">
        <button
          onClick={() => { window.location.hash = "admin"; }}
          className="text-xs text-gray-400 hover:text-purple-600 transition-colors"
        >
          Admin
        </button>
      </div>
    </div>
  );
}